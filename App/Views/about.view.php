        <h3>Hailing frequencies open, captain...</h3>
        <p>
            "silentrunning" is a new virtual social gathering place (formerly known as social networks) designed to be
            focused on the people who use it rather than profit making or other business
            interests.
        </p>
        <h4>We're people, now lawyers</h4>
        <p>
            Have you ever read the privacy policy of a site like Facebook, or Google? Chances are
            you haven't, but even if you did, you probably didn't understand all of it. Privacy
            policies are typically written by lawyers for lawyers to protect the interests of the 
            company rather than the interests of those the company serves. I want the policy of
            silentrunning to be something we can all understand and that protects the interests
            of the users (that's you!).
        </p>
        <h4>Mission</h4>
        <p>
        <ol>
            <li>Don't be evil...no really.</li>
            <li>You own your content, and you always will.</li>
            <li>You can delete some or all of your content at any time.</li>
            <li>Your content will never be sold or shared without your explicit permission. (This means no search engine indexing)</li>
            <li>Your information will never be sold or shared.</li>
            <li>If any of our policies are unclear, we will make them clear.</li>
            <li>You can provide as much or as little information as you want. (A unique 'handle' and password are required)</li>
        </ol>
        </p>
        <h4>Technical Goodies...</h4>
        <p>
        <ul>
            <li>Passwords are stored using the <a href="http://en.wikipedia.org/wiki/SHA-2" target="_blank">SHA512 one-way hashing algorithm</a> and are salted with a 256-bit key.</li>
            <li>All users are given a database user account with read access to the users table.</li>
            <li>Your user account acts as a lynchpin for all your data. If you choose to delete your account, the delete will automatically and irreversibly cascade to all your content.</li>
            <li>Any content encryption is done using <a href="http://en.wikipedia.org/wiki/Advanced_Encryption_Standard" target="_blank">256-bit AES.</a></li>
        </ul>
        </p>
